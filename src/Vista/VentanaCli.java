    private void tblclientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblclientesMouseClicked
        int sel = tblclientes.getSelectedRow();
        if(sel == -1){
            if(tblclientes.getRowCount()==0){
                JOptionPane.showMessageDialog(this,"No hay registros");
            }
        }else {
            txtcodigo.setText(tblclientes.getValueAt(sel, 0).toString());
            txtnombre.setText(tblclientes.getValueAt(sel, 1).toString());
            txtcc.setText(tblclientes.getValueAt(sel, 2).toString());
            txttelefono.setText(tblclientes.getValueAt(sel, 3).toString());
            txtdireccion.setText(tblclientes.getValueAt(sel, 4).toString());
            btnactualizar.setEnabled(true);
            btneliminar.setEnabled(true);
            btnagregar.setText("Cancelar");
            btnagregar.setActionCommand("cancelar");
        }
    }//GEN-LAST:event_tblclientesMouseClicked


