    //"Crear" tabla de produccion
    public void cargarProduccion(ArrayList<Produccion> listaProdu, ArrayList<MateriaPrima> matpri) {
        limpiarTablaProduccion();
        for (int i = 0; i < listaProdu.size(); i++) {
            
            int codMatPri = listaProdu.get(i).getMateriaPrima();
            String nomMatPri = "";
            for (int j = 0; j < matpri.size(); j++) {
                if(matpri.get(j).getCodigoMatPri() == codMatPri){
                    nomMatPri = matpri.get(j).getNombreMatPri();
                }
            }
            
            mProdu.addRow(new Object[]{
                nomMatPri,
                //listaProdu.get(i).getMateriaPrima(),
                listaProdu.get(i).getCantidadMatPrima(),
                "",});
        }
    }

    //"Crear" tabla de  Receta
    public void cargarReceta(ArrayList<Produccion> receta, ArrayList<MateriaPrima> matpri) {
        limpiarTablaReceta();
        for (int i = 0; i < receta.size(); i++) {
            
            int codMatPri = receta.get(i).getMateriaPrima();
            String nomMatPri = "";
            for (int j = 0; j < matpri.size(); j++) {
                if(matpri.get(j).getCodigoMatPri() == codMatPri){
                    nomMatPri = matpri.get(j).getNombreMatPri();
                }
            }
            mReceta.addRow(new Object[]{
                receta.get(i).getCodigoProdu(),
                receta.get(i).getMateriaPrima(),
                nomMatPri,
                receta.get(i).getCantidadMatPrima()});
        }
    }

    //Cargar los datos de les ComboBox
    public void cargarProducto(ArrayList<Producto> listaPro) {
        mPro1.removeAllElements();
        mPro2.removeAllElements();
        for (int i = 0; i < listaPro.size(); i++) {
            mPro1.addElement(listaPro.get(i).getNombrePro());
            mPro2.addElement(listaPro.get(i).getNombrePro());
        }
    }

    public void cargarMatPri(ArrayList<MateriaPrima> listaMatPrimas) {
        mMatPri.removeAllElements();
        for (int i = 0; i < listaMatPrimas.size(); i++) {
            mMatPri.addElement(listaMatPrimas.get(i).getNombreMatPri());
        }
    }

